package me.ssagan.service;

public class Calculator {
    public static int sum(int a, int b){
        int result = a + b;
        return result;
    }

    public static int multiply(int a, int b){
        int result = a * b;
        return result;
    }

    public static int subtract(int a, int b){
        int result = a - b;
        return result;
    }
}
